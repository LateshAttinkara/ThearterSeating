package com.theatresection;

public class Customer {
	private String personName;
	private int numSeatRequested;
	
	public String getPersonName() {
		return personName;
	}
	public void setPersonName(String personName) {
		this.personName = personName;
	}
	public int getNumSeatRequested() {
		return numSeatRequested;
	}
	public void setNumSeatRequested(int numSeatRequested) {
		this.numSeatRequested = numSeatRequested;
	}
	
	
}
